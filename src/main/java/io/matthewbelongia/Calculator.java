public class Calculator {
    public int sum(int value1, int value2){
        return value1 + value2;
    }
    public int mult(int value1,int value2){
        return value1 * value2;
    }
    public int div(int value1,int value2){
        return value1 / value2;
    }
    public int diff(int value1,int value2){
        return value1 - value2;
    }

}


